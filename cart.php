    var id = 111;
    var totalPrice = 0;
    $(document).ready(function() {
        $('a').click(function() {
            $('html, body').animate({
                scrollTop: $($.attr(this, 'href')).offset().top
            }, 500);
            return false;
        });
        $(".add-to-cart").click(function() {
            var noOfItems = 1;
            var itemName = $(this).prev().html();
            var itemPrice = parseInt($(this).next().next().html().substring(3));
            var itemsInCartTags = $(".cart-items").children();
            var sum = 0;
            for (var i = 0; i < itemsInCartTags.length; i++) {
                child = itemsInCartTags[i].children
                mulItemName = child[1].innerHTML
                var mulItemPrice = parseInt(child[4].innerHTML.substring(3));
                // alert(mulItemPrice);
                if (mulItemName == itemName) {
                    var mulItem = itemsInCartTags[i];
                    var mulItemId = mulItem.id;
                    var noOfItems = $('#' + mulItemId + ' input[type="text"]').attr("value");
                    noOfItems++;
                    mulItemPrice = parseInt(itemPrice) + parseInt(mulItemPrice);
                    break;
                };
            };


            var addItem = document.getElementById("cart");
            if (noOfItems == 1) {
                addItem.innerHTML += '<div class="selected-item" id="' + id + '"><input type="hidden" value="' + itemName + '"><p  class="selected-item-name">' + itemName + '</p><input type="text" value="' + 1 + '" disabled><span class="remove-item">X</span><p class="selected-item-price">Rs.' + itemPrice + '/-</p></div>';
                id++;
            } else {

                $('#' + mulItemId + ' input[type="text"]').attr("value", noOfItems);
                var itemPrice = parseInt($(this).next().next().html().substring(3));
                child[4].innerHTML = "Rs." + mulItemPrice + "/-";
            }
            if (document.getElementById("value").innerHTML != "") {
                currPrice = parseInt(document.getElementById("value").innerHTML.substring(3));
                totalPrice = parseInt(currPrice) + parseInt(itemPrice);
            } else {
                var priceTag = $("p").filter(".selected-item-price");
                for (var i = 0; i < priceTag.length; i++) {
                    sum += parseInt(priceTag[i].innerHTML.substring(3));
                };
                totalPrice = parseInt(noOfItems * sum);
            }
            document.getElementById("value").innerHTML = "Rs." + totalPrice + "/-";
        });

        $(document).on("click", ".remove-item", function() {
            var cartItemPrice = parseInt($(this).next().html().substring(3));
            var currCartValue = parseInt($("#value").html().substring(3));
            document.getElementById("value").innerHTML = "Rs." + (currCartValue - cartItemPrice) + "/-";
            $(this).parent().remove();
        });
    });
    </script>
				

				
